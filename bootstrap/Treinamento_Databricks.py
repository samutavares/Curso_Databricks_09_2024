# MAGIC ## **Databricks** 
# MAGIC É uma plataforma de análise baseada em nuvem otimizada para o processamento de grandes conjuntos de dados. Ela integra várias ferramentas de análise de dados e machine learning, facilitando a colaboração entre **cientistas de dados**, **engenheiros de dados** e **analistas de negócios**. No geral é uma plataforma abrangente projetada para simplificar e acelerar fluxos de trabalho para produtos de dados em escala. Aqui esta uma análise dos principais componentes da plataforma Databricks:
# MAGIC - **Apache Spark**
# MAGIC     - fortemente integrado o que permite processamento de dados escalável e distruibuído. Ideal para lidar com grande cargas de dados
# MAGIC - **Colaboração**
# MAGIC     - fornece notebooks onde equipes podem trabalhar juntos em tempo real. Suporte várias linguagens como Python, Scala, R e Java.
# MAGIC - **Machine Learning and AI**
# MAGIC     - permite criar, treinar e implantar modelos usando ferramentas como o MLflow para rastreamento de experimentos e gerenciamento de modelos.
# MAGIC - **Data Lakehouse**
# MAGIC     - combinação das melhroes recursos de datalake e data warehouse. Suporta dados estruturados, semiestruturados e não estruturados, permitindo que o fluxo de trabalho analítico e de AI sejam construídos em armazenamento de dados unificado
# MAGIC - **Delta Lake**
# MAGIC     - camada de armazenamento open-source que traz transações **A**tomicidade, **C**onsistência, **I**solamento e **D**urabilidade, tratamento escalável de metadados e unificação de streaming e processamento de dados em lote (batch)  
# MAGIC     ![delta_img](files/tables/imgs/MikeCornell_Databricks_1_1609643048644.png)
# MAGIC - **Pipelines de dados e ETL**
# MAGIC     - fornece uma plataforma para orquestrar e automatizar facilmente fluxos de trabalho de dados e oferece suporte a integração com ferramentas como Azure Data Factory para orquestrações mais complexas
# MAGIC - **Escalabilidade e desempenho**
# MAGIC     - roda em serviços de nuvem como Azure, AWS e GCP, fornecendo a combinação perfeita para conjuntos de dados em crescimento
# MAGIC - **Segurança e Governança**
# MAGIC     - oferece recursos de segurança integrados como Access Management (IAM), criptografia de dados e controles de acessos destalhados para garantir a conformidade e a governança de dados em todas as organizações
# MAGIC - **Integração com Cloud Ecosystems**: oferece um suporte a integraçãoes com serviços nativos como:
